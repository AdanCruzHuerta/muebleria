	{
		if (Request::ajax())
		{
    		$mensaje = new Mensaje;

    		$mensaje->nombre 	= Input::get('nombre');

    		$mensaje->correo 	= Input::get('email');

    		$mensaje->telefono 	= Input::get('telefono');

    		$mensaje->mensaje 	= Input::get('descripcion');

    		if( $mensaje->save() )
    		{
    			return Response::json(true);
    		}
    		else
    		{
    			return Response::json(false);
    		}

		}
	}


